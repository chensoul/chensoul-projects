package com.chensoul.constant;

/**
 * <p>PathConstants interface.</p>
 *
 * @author chensoul
 * @version $Id: $Id
 */
public interface PathConstants {

    /**
     * Slash char
     */
    char SLASH_CHAR = '/';

    /**
     * Back Slash char
     */
    char BACK_SLASH_CHAR = '\\';

    /**
     * Slash : <code>"/"</code>
     */
    String SLASH = "/";

    /**
     * Double Slash : <code>"//"</code>
     */
    String DOUBLE_SLASH = SLASH + SLASH;

    /**
     * Back Slash : <code>"\"</code>
     */
    String BACK_SLASH = "\\";

}
